package math

func MathBool(num1, num2 int) bool {
	return num1 == num2
}
func MathMult(num1, num2 int) int {
	return num1 * num2
}
func MathPlus(num1, num2 int) int {
	return num1 + num2
}
func MathMinus(num1, num2 int) int {
	return num1 - num2
}
func MathDilen(num1, num2 int) int {
	return num1 / num2
}
